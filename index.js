const resultsContainer = document.getElementById('results');
const unitSelect = document.getElementById('unitSelect');

function loadQuiz() {
    const selectedUnit = unitSelect.value;
    const questions = units[selectedUnit];

    if (!questions) {
        alert('Invalid unit selection.');
        return;
    }

    let output = '';

    questions.forEach((currentQuestion, questionNumber) => {
        const options = [];
        for (let i = 0; i < currentQuestion.options.length; i++) {
            options.push(
                `<label>
                    <input type="radio" name="question${questionNumber}" value="${String.fromCharCode(65 + i)}">
                    ${currentQuestion.options[i]}
                </label><br>`
            );
        }

        output += `
            <div class="question">
                <p>${questionNumber + 1}. ${currentQuestion.question}</p>
                ${options.join('')}
            </div>
        `;
    });

    quizContainer.innerHTML = output;
}

function submitQuiz() {
    const answerContainers = quizContainer.querySelectorAll('.question');
    let score = 0;
    const wrongAnswers = [];

    const selectedUnit = unitSelect.value;
    const questions = units[selectedUnit];

    if (!questions) {
        alert('No quiz loaded.');
        return;
    }

    questions.forEach((currentQuestion, questionNumber) => {
        const answerContainer = answerContainers[questionNumber];
        const selector = `input[name=question${questionNumber}]:checked`;
        const userAnswer = (answerContainer.querySelector(selector) || {}).value;

        if (userAnswer === currentQuestion.answer) {
            score++;
        } else {
            wrongAnswers.push(questionNumber + 1);
        }
    });

    resultsContainer.innerHTML = `<h2>Your score for ${selectedUnit.toUpperCase()} is: ${score}/${questions.length}</h2>`;
    
    if (wrongAnswers.length > 0) {
        resultsContainer.innerHTML += '<h3>You got these questions wrong:</h3>';
        wrongAnswers.forEach(qNumber => {
            resultsContainer.innerHTML += `<p>Question ${qNumber}</p>`;
        });
    } else {
        resultsContainer.innerHTML += '<h3>Congratulations! You got all questions correct.</h3>';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    loadQuiz();
});